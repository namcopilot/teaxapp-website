function pickLanguage() {
    const urlLang = new URLSearchParams(window.location.search).get("lang");
    if (urlLang && I18N[urlLang]) return urlLang;
    const stored = window.localStorage.getItem("timecalc-site-lang");
    if (stored && I18N[stored]) return stored;
    const browser = (navigator.language || "en").slice(0, 2).toLowerCase();
    return I18N[browser] ? browser : "en";
}

function getPreviewSrc(lang, index) {
    const folder = PREVIEW_FOLDERS[lang] || PREVIEW_FOLDERS.en;
    const file = PREVIEW_FILES[index] || PREVIEW_FILES[0];
    return `./imgs/previews/${folder}/${encodeURIComponent(file)}`;
}

function updatePreviewImages(lang) {
    document.querySelectorAll("[data-preview-index]").forEach((img) => {
        const index = Number(img.dataset.previewIndex);
        const fallbackSrc = getPreviewSrc("en", index);
        img.onerror = () => {
            if (img.getAttribute("src") !== fallbackSrc) {
                img.setAttribute("src", fallbackSrc);
            }
        };
        img.setAttribute("src", getPreviewSrc(lang, index));
    });
}

